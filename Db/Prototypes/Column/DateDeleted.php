namespace Arikaim\Core\Db\Prototypes\Column;

use Arikaim\Core\Interfaces\BlueprintPrototypeInterface;

/**
 * DateDeleted column prototype class
*/
class DateDeleted implements BlueprintPrototypeInterface
{
    /**
     * Build column
     *
     * @param Arikaim\Core\Db\TableBlueprint $table
     * @param mixed $options
     * @return void
     */
    public function build($table,...$options)
    {
        $table->bigInteger('date_deleted')->nullable(true); 
        $table->index('date_deleted');             
    }
}
